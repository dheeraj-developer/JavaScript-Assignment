//Strings---A string (or a text string) is a series of characters written in single or double quotes.
var food = "Briyani";   // Using double quotes
var food = 'Biriyani';   // Using single quotes
console.log(food); //prints Biriyani

//Numbers---Numbers can be written with, or without decimals.
var a = 34.00;     // Written with decimals
var b = 34;        // Written without decimals
console.log(a); //prints 34
console.log(b); //prints 34

//Booleans---Booleans can only have two values: true or false.
var x = 5;
var y = 5;
var z = 6;
console.log(x == y)       // Returns true
console.log(x == z)       // Returns false

//Arrays---JavaScript arrays are written with square brackets with array items are separated by commas.
var favColor = ['blue','black','pink'];
console.log(favColor);

//Objects---written with curly braces {}.Object properties are written as name:value pairs, separated by commas.
var person = {firstName:"John", lastName:"Doe", age:50, eyeColor:"blue",};
console.log(person);